def save_stuff(stuff):

	files = ["BC", "FC", "LS", "LV", "SF", "RC", "TR"]
	count = 0

	im = Image.open("gtasa-blank-1.0.jpg")
	draw = ImageDraw.Draw(im)
	render.draw_regions(im, draw)

	for name in files:
		count = 0
		with io.open(SCRIPTFILES_PATH + MAPSDATA_PATH + name + "/trees.tpl", "w") as f:

			for i in stuff:
				if IsPointIn(i[1], i[2], name):
					f.write("CreateTree(%s, %f, %f, %f);\n"%(i[0], i[1], i[2], i[3]))
					count += 1
					draw.ellipse([int(i[1] + 3000) - 8, int(6000 - (i[2] + 3000)) - 8, int(i[1] + 3000) + 8, int(6000 - (i[2] + 3000)) + 8], outline=(255, 255, 255), fill=(0, 128, 0))

		print("Saved", count, "trees for", name)

	# clear up the leftovers and dump them in GEN, should be empty really

	with io.open(SCRIPTFILES_PATH + MAPSDATA_PATH + "GEN/trees.tpl", "w") as f:
		for i in stuff:
			region = 0
			for name in files:
				if IsPointIn(i[1], i[2], name):
					region = 1
					break

			if region == 0:
				f.write("CreateTree(%s, %f, %f, %f);\n"%(i[0], i[1], i[2], i[3]))
				count += 1
				draw.ellipse([int(i[1] + 3000) - 4, int(6000 - (i[2] + 3000)) - 4, int(i[1] + 3000) + 4, int(6000 - (i[2] + 3000)) + 4], outline=(255, 255, 255), fill=(20, 80, 20))

	im.save("output.bmp")


